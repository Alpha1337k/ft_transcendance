import { Column, Entity, ManyToMany, PrimaryGeneratedColumn } from 'typeorm';
import { UserEntity } from '../user/entities/user.entity';

@Entity()
export class Match {
	@PrimaryGeneratedColumn('increment')
	matchid: number;

	@ManyToMany(() => UserEntity, (player) => player.history)
	players: UserEntity[];

	@Column()
	p1Score: number;

	@Column()
	p2Score: number;
}
